# Numeric Data type

print("Python has three numeric types: int, float, and complex")

myValue=1
print(myValue)

print(type(myValue))

print(str(myValue) + " is of the data type " + str(type(myValue)))

my_value = 3.14
print(my_value)
print(type(my_value))

print(str(my_value) +  " is of the data type " + str(type(my_value)))

MyValue = 5j
print(MyValue)
print(type(MyValue))
print(str(MyValue) + " is of the data type " + str(type(MyValue)))

